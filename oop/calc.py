class Calc:
    def fnSumm(self, num1, num2):
        result = num1+num2
        return result

    def fnDiv(self, num1, num2):
        result = num1/num2
        return result

    def fnSubs(self, num1, num2):
        result = num1-num2
        return result

    def fnMult(self, num1, num2):
        result = num1*num2
        return result
